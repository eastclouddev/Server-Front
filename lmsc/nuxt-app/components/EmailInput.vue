<template>
  <v-container class="d-flex justify-space-between">
    <v-row class="align-center">
      <v-col class="text-left text-md-left">
        <label style="font-size: 1em; font-weight: bold" color="#242424"
          >メールアドレス</label
        >
      </v-col>
      <v-col class="text-center text-md-right">
        <v-card flat class="d-flex flex-column" width="25rem">
          <v-sheet
            :class="{ error: errors.email }"
            class="my-0 pr-4 pb-4 pl-4"
            color="#EBEBEB"
          >
            <v-text-field
              hide-details="auto"
              placeholder="XXXXXXX@example.com"
              variant="plain"
              type="email"
              v-model="internalEmail"
            ></v-text-field>
          </v-sheet>
          <p class="error_message">{{ errors.email }}</p>
        </v-card>
      </v-col>
    </v-row>
  </v-container>
</template>

<script setup>
import { computed, watch } from 'vue'
import { useField, useForm } from 'vee-validate'
import { object, string, setLocale } from 'yup'

const props = defineProps({
  modelValue: String,
})
const emit = defineEmits(['update:modelValue'])

setLocale({
  mixed: {
    required: 'メールアドレスを入力してください。',
  },
  string: {
    email: 'メールアドレスの形式が正しくありません。',
  },
})

const schema = object({
  email: string().required().email().label('メールアドレス'),
})

const { errors } = useForm({
  validationSchema: schema,
})

const { value: email } = useField('email')

const internalEmail = computed({
  get: () => props.modelValue,
  set: value => emit('update:modelValue', value),
})

watch(internalEmail, newVal => {
  email.value = newVal
})
</script>

<style lang="scss" scoped>
.error {
  border: 1px solid red;
  border-radius: 5px;
}

.error_message {
  color: #ff0000;
  font-size: 0.75em;
}
@media screen and (max-width: 768px) {
  .error_message {
    font-size: 1em;
  }
}
</style>
